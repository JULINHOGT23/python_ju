# desarrollo de un algoridmo de la suma y multiplicación con dos varibles o números

# ingresamos los valores
v1 = int(input("ingrese el primer valor 1 : "))
v2 = int(input("ingrese el primer valor 2 : "))

# calculamos los valores de la suma
suma = v1 + v2

# calculamos los valores dela multiplicacion
multiplicacion = v1 * v2

# mostramos los resultados del algoridmo
print("la suma de valores", suma)
print("la resta de valores", multiplicacion)
